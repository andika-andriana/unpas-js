var jmlAngkot = 10;
    angkotBeroperasi = 6;
    noAngkot = 1;

for(noAngkot = 1; noAngkot <= jmlAngkot; noAngkot++){
  if(noAngkot <= angkotBeroperasi){
    console.log('Angkot No. '+noAngkot+' berfungsi dengan baik');
  }else{
    console.log('Angkot No. '+noAngkot+' tidak berfungsi dengan baik');
  }
}
